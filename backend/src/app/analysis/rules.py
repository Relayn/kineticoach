# --- Пороги для углов (в градусах) ---

# Угол, при пересечении которого вниз/вверх меняется состояние UP/DOWN
# Откалибровано по видео: в полный рост угол ~175-178. 170 - надежный порог.
REP_TRANSITION_ANGLE: float = 170.0

# Границы "хорошей" глубины приседа: угол в коленном суставе
# Откалибровано по видео: идеальная глубина ~80-85 градусов.
SQUAT_DEPTH_GOOD_MIN: float = 75.0  # Ниже этого - слишком глубоко
SQUAT_DEPTH_GOOD_MAX: float = 110.0  # Выше этого - недостаточно глубоко

# Наклон корпуса: угол между бедром и торсом
# Откалибровано по видео: хороший угол ~80-90 градусов.
# 75 - порог для умеренного наклона.
BODY_BEND_FORWARD_THRESHOLD: float = 75.0  # Меньше этого - сильный наклон вперед

# Прогиб спины: угол между бедром и торсом в ВЕРХНЕЙ точке
# Откалибровано по видео: вверху угол ~168-170. 175 - порог для избыточного выпрямления.
BODY_BEND_BACKWARDS_THRESHOLD: float = 175.0

# --- Пороги для координат ---

# Выход коленей за носки: разница по горизонтальной оси X между коленом и лодыжкой.
# Значение является долей от ширины плеч (для масштабирования).
KNEE_OVER_TOE_THRESHOLD: float = 0.3  # 30% от ширины плеч

# --- Порог видимости ---

# Минимальная уверенность модели, чтобы мы доверяли координатам точки
MIN_VISIBILITY_THRESHOLD: float = 0.5
